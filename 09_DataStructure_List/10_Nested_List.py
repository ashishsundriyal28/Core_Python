# ==================================================================================================
#                                   NESTED LIST
# ==================================================================================================

l = [10, 20, [30, 40]]
print(l[0])
print(l[1])

print(l[2]) # Output : [30, 40]

print(l[2][0]) # Output : 30
print(l[2][1]) # Output : 40


# --------------------------------------------------------------------------------------------------
# Nested list as Matrix
# --------------------------------------------------------------------------------------------------

l = [[10, 20, 30], [40, 50, 60], [70, 80, 90]]

print(l)
print(l[0])
print(l[1])
print(l[2])

print("Elements row-wise : ")

for x in l:
    print(x)
'''
OUTPUT : 

[10, 20, 30]
[40, 50, 60]
[70, 80, 90]
'''

print("Print Elements Matrix style : ")


for x in l:
    for y in x:
        print(y, end=" ")
    print()

'''
OUTPUT : 

10 20 30 
40 50 60 
70 80 90 
'''